#include "ft_printf.h"

int	ft_putstr(char *str)
{
	int	len;

	if (str == NULL)
		str = "(null)";
	len = 0;
	while (str[len] != '\0')
	{
		write(1, &str[len], 1);
		len++;
	}
	return (len);
}
