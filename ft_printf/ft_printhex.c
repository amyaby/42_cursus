int	ft_printhex(unsigned int n, char *str)
{
	int	i;

	i = 0;
	if (n >= 0 && n < 16)
		i += ft_putchar(str[n]);
	if (n >= 16)
	{
		i += ft_printhex(n / 16, str);
		i += ft_printhex(n % 16, str);
	}
	return (i);
}
